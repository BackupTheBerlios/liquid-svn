static QImage liquid_findImage( const QString& name )
{
    for (int i=0; liquid_embed_image_vec[i].data; i++) {
	if ( QString::fromUtf8(liquid_embed_image_vec[i].name) == name ) {
	    QByteArray baunzip;
	    baunzip = qUncompress( liquid_embed_image_vec[i].data, liquid_embed_image_vec[i].compressed );
	    QImage img((uchar*)baunzip.data(),
			liquid_embed_image_vec[i].width,
			liquid_embed_image_vec[i].height,
			liquid_embed_image_vec[i].depth,
			(QRgb*)liquid_embed_image_vec[i].colorTable,
			liquid_embed_image_vec[i].numColors,
			QImage::BigEndian);    
	    img = img.copy();
	    if ( liquid_embed_image_vec[i].alpha )
		img.setAlphaBuffer(TRUE);
	    return img;
        }
    }
    return QImage();
}

class MimeSourceFactory_Liquid : public QMimeSourceFactory
{
public:
    MimeSourceFactory_Liquid() {}
    ~MimeSourceFactory_Liquid() {}
    const QMimeSource* data( const QString& abs_name ) const {
	const QMimeSource* d = QMimeSourceFactory::data( abs_name );
	if ( d || abs_name.isNull() ) return d;
	QImage img = liquid_findImage( abs_name );
	if ( !img.isNull() )
	    ((QMimeSourceFactory*)this)->setImage( abs_name, img );
	return QMimeSourceFactory::data( abs_name );
    };
};
static QMimeSourceFactory* factory = 0;
void qInitImages_Liquid()
{
    if ( !factory ) {
	factory = new MimeSourceFactory_Liquid;
	QMimeSourceFactory::defaultFactory()->addFactory( factory );
    }
}
void qCleanupImages_Liquid()
{
    if ( factory ) {
	QMimeSourceFactory::defaultFactory()->removeFactory( factory );
	delete factory;
	factory = 0;
    }
}
class StaticInitImages_Liquid
{
public:
    StaticInitImages_Liquid() { qInitImages_Liquid(); }
    ~StaticInitImages_Liquid() { qCleanupImages_Liquid(); }
};
